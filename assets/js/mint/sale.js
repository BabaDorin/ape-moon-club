// Do not try to overrite these values. These verifications also exists within the contract.
// There is no need to try overcoming them.
const FREE_PER_WALLET_ON_WEBSITE = 2;

const mint = async (e) => {
    if (typeof window.ethereum == 'undefined')
        return;

    let values = document.getElementById('amount').getElementsByTagName('span');

    let cnt = 0;
    
    for(i = 0; i < values.length; i++)
        if (values[i] != undefined && window.getComputedStyle(values[i]).display != 'none')
            cnt = values[i].innerHTML;

    if (cnt < MIN_AMOUNT_ON_WEBSITE || cnt > MAX_AMOUNT_ON_WEBSITE) {
        showError("Error: You're trying to mint an invalid number of items. Min: 1, Max: 15.");
        return;
    }

    try {
        const accounts = await window.ethereum.request({ method: 'eth_requestAccounts' })
        
        const web3 = new Web3(window.ethereum);
        await window.ethereum.enable();

        const contract = new web3.eth.Contract(abi, CONTRACT_ADDR, { gas: 3000000 });
        const isActive = await contract.methods.getIsPublicSaleActive().call();
        
        if (!isActive) {
            showError('Sale is not active yet', 'Please, wait for the contract owner to activate the sale.');
            return;
        }

        const maxSupply = await contract.methods.maxSupply().call();
        let totalSupply = await contract.methods.totalSupply().call();
        
        const address_balance = await contract.methods.balanceOf(accounts[0]).call();

		const maxFreeForMint = 2000;

		const alreadyMinted = await contract.methods.getFreeNftAlreadyMinted().call();

		console.log('free already minted: ' + alreadyMinted);
        
        let finalPrice = 0;
		let customerOrderLabel = document.getElementById('customer-order');

		let checkAgainst = FREE_PER_WALLET_ON_WEBSITE;

		if (alreadyMinted > maxFreeForMint) checkAgainst = 0;

		if (checkAgainst == 0) {
			// mint all with eth

			const price = await contract.methods.getSalePrice().call();
			
			finalPrice = cnt * price;
			let finalPriceDisplay = finalPrice / 1000000000000000000;

			customerOrderLabel.textContent = '0 free, ' + cnt + ' paid. Final price: ' + finalPriceDisplay + ' ETH + gas.';
		} 
		else 
		{
			if (address_balance + cnt > checkAgainst){
            
				const price = await contract.methods.getSalePrice().call();
	
				if (address_balance > checkAgainst) {
					// Paid mint for all
					finalPrice = cnt * price;
					let finalPriceDisplay = finalPrice / 1000000000000000000;
	
					customerOrderLabel.textContent = '0 free, ' + cnt + ' paid. Final price: ' + finalPriceDisplay + ' ETH + gas.';
				}
				else
				{
					// Mixed: Left free - for free, the rest - for ETH.
					const freeMint = checkAgainst - address_balance;
					const paidMint = cnt - freeMint;
	
					finalPrice = paidMint * price;
					customerOrderLabel.textContent = freeMint + ' free, ' + paidMint + ' paid. Final price: ' + finalPrice / 1000000000000000000 + ' ETH + gas.';
				}
			}
			else {
				// Mint all for free
				finalPrice = 0;
	
				customerOrderLabel.textContent = cnt + ' free, just pay for the gas.';
			}
		}

        const gas = await estimateGas(contract, cnt, finalPrice, accounts[0]);

        await contract.methods
            .mint(cnt)
            .send({ value: finalPrice.toString(), from: accounts[0], gas: gas });

		showError("Transaction successful!", "You can view your NFTs on opensea ❤️");

        totalSupply = await contract.methods.totalSupply().call();
        document.getElementById("tokens-available").innerHTML = totalSupply + "/" + maxSupply;
    } 
    catch(e) {
        showError('Error', e.message);
    }
}

async function estimateGas (contract, cnt, value, account) {
    const approximateGas = await contract.methods
        .mint(cnt)
        .estimateGas({ value: value.toString(), from: account })
        * 1.2;

    return Math.round(approximateGas);
}
